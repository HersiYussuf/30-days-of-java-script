// Function to find MERN stack developers
function findMernStackDevelopers(developers) {
    const mernStackDevelopers = [];

    for (const developer in developers) {
        const skills = developers[developer].skills;
        if (
            skills.includes("MongoDB") &&
            skills.includes("Express") &&
            skills.includes("React") &&
            skills.includes("Node")
        ) {
            mernStackDevelopers.push(developers[developer]);
        }
    }

    return mernStackDevelopers;
}

